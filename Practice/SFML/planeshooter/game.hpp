/**********************************************************************
***********************************************************************
*                                                                     *
* GAME                                                                *
*                                                                     *
* Description:                                                        *
*       GAME class, will handle game related inputs                   *
*                                                                     *
* Private Methods:                                                    *
*       None                                                          *
*                                                                     *
* Protected Methods:                                                  *
*       sf::RenderWindow mWindow; Game window                         *
*       sf::CircleShape mPlayer;  Player object                       *
*                                                                     *
* Public Methods:                                                     *
*       GAME                 *                                        *
*       Run                  * See notes                              *
*       ProcessEvents        * below on                               *
*       Update               * usage                                  *
*       Render               *                                        *
*                            *                                        *
***********************************************************************
**********************************************************************/

class GAME
{
    protected:
        sf::RenderWindow mWindow;
        sf::CircleShape mPlayer;

    public:

    /**********************************************************************
    *                                                                     *
    * GAME                                                                *
    *                                                                     *
    * Description:                                                        *
    *       Holds the game window, player object and anyother objects in  *
    *       game.                                                         *
    *                                                                     *
    * Method Variables:                                                   *
    *                                                                     *
    * Use:                                                                *
    *   Creates our game object                                           *
    *                                                                     *
    **********************************************************************/
        GAME(): mWindow(
            sf::VideoMode(640,480), "Game Window",
            sf::Style::Default), mPlayer()
        {
            mPlayer.setRadius(40.f);
            mPlayer.setPosition(100.f, 100.f);
            mPlayer.setFillColor(sf::Color::Green);
        }
        void Run()
        {
            while (mWindow.isOpen())
            {
                ProcessEvents();
                Update();
                Render();
            }

        }
        void ProcessEvents()
        {
            sf::Event event;
            while (mWindow.pollEvent(event))
            {
                if (event.type == sf::Event::Closed)
                {
                    mWindow.close();
                }
            }
        }
        void Update();
        void Render();

};